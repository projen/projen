import { synthSnapshot, TestProject } from "../util";

describe("workflow steps", () => {
  test("can synth all options", () => {
    // GIVEN
    const project = new TestProject();
    const wf = project.github?.addWorkflow("my-workflow");

    // WHEN
    wf?.addJob("foobar", {
      permissions: {},
      steps: [
        {
          if: "${{ true }}",
          id: "foobar",
          continueOnError: true,
          env: {
            ONE: "two",
          },
          name: "test name",
          run: 'echo "test"',
          shell: "bash",
          timeoutMinutes: 10,
          workingDirectory: "/",
        },
      ],
    });

    // THEN
    const snap = synthSnapshot(project)[".github/workflows/my-workflow.yml"];
    expect(snap).toMatchInlineSnapshot(`
      "# ~~ Generated by projen. To modify, edit .projenrc.js and run "npx projen".

      name: my-workflow
      on: {}
      jobs:
        foobar:
          permissions: {}
          steps:
            - name: test name
              id: foobar
              if: \${{ true }}
              env:
                ONE: two
              run: echo "test"
              shell: bash
              continue-on-error: true
              timeout-minutes: 10
              working-directory: /
      "
    `);
  });

  test("can synth background, wait, wait-all, cancel and parallel", () => {
    // GIVEN
    const project = new TestProject();
    const wf = project.github?.addWorkflow("my-workflow");

    // WHEN
    wf?.addJob("foobar", {
      permissions: {},
      steps: [
        {
          id: "server",
          name: "Start server",
          run: "npm start",
          background: true,
        },
        {
          id: "build-frontend",
          name: "Build frontend",
          run: "npm run build:frontend",
          background: true,
        },
        {
          id: "build-backend",
          name: "Build backend",
          run: "npm run build:backend",
          background: true,
        },
        {
          name: "Wait for both builds",
          wait: ["build-frontend", "build-backend"],
        },
        {
          name: "Wait for the server",
          wait: ["server"],
        },
        {
          name: "Wait for everything else",
          waitAll: true,
        },
        {
          name: "Stop the server",
          cancel: "server",
        },
        {
          parallel: [
            { name: "Build docs", run: "npm run build:docs" },
            { name: "Build assets", run: "npm run build:assets" },
          ],
        },
      ],
    });

    // THEN
    const snap = synthSnapshot(project)[".github/workflows/my-workflow.yml"];
    expect(snap).toMatchInlineSnapshot(`
      "# ~~ Generated by projen. To modify, edit .projenrc.js and run "npx projen".

      name: my-workflow
      on: {}
      jobs:
        foobar:
          permissions: {}
          steps:
            - name: Start server
              id: server
              run: npm start
              background: true
            - name: Build frontend
              id: build-frontend
              run: npm run build:frontend
              background: true
            - name: Build backend
              id: build-backend
              run: npm run build:backend
              background: true
            - name: Wait for both builds
              wait:
                - build-frontend
                - build-backend
            - name: Wait for the server
              wait:
                - server
            - name: Wait for everything else
              wait-all: true
            - name: Stop the server
              cancel: server
            - parallel:
                - name: Build docs
                  run: npm run build:docs
                - name: Build assets
                  run: npm run build:assets
      "
    `);
  });
});
